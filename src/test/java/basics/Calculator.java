package basics;

public class Calculator {
    public int add (int firstnum, int secontnum) {
        return firstnum + secontnum;
    }

    public int substract (int firstnum, int secontnum) {
        return firstnum - secontnum;
    }

    public int divide (int firstnum, int secontnum) {
        return firstnum / secontnum;
    }

    public int miltiply (int firstnum, int secontnum) {
        return firstnum * secontnum;
    }
}
